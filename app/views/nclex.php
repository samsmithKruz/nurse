        <h2>What is <span>NCLEX</span>?</h2>
        <img src="./assets/nclex_study.png" alt="" />
        <p>
          The NCLEX exam, also known as the National Council Licensure
          Examination, is a standardized exam that each State Board of Nursing
          uses to determine whether or not a candidate is prepared for
          entry-level nursing practice.
        </p>
        <p>
          But your first step is to successfully complete an accredited nursing
          degree. NCLEX Eligibility & Registration In order to take the NCLEX
          exam, you must apply for a Nursing license from a State Board of
          Nursing. That board will determine whether or not you meet its
          criteria for NCLEX Examination-Eligibility. You must then register to
          take the exam.
        </p>
        <p>
          Once you’ve applied for a license from your state board of nursing,
          you will receive an NCLEX Examination Candidate Bulletin in the mail.
          You may then register with the National Council of State Boards of
          Nursing’s testing service by phone, or by filling out a mail-in
          registration form.
        </p>
        <p>
          Once your state board of nursing has verified your eligibility to take
          the NCLEX examination, you will receive an Authorization to Take the
          Test (ATT), along with a list of testing centers and instructions for
          how to schedule an appointment to take the examination. The cost to
          take the NCLEX is $200.
        </p>
        <h4>Exam Day</h4>
        <p>
          In this section, you will find an in-depth look at the candidate exam
          day process and the rules to be followed during the administration of
          your exam at a Pearson Professional Center. Below are a few things to
          think about on the exam day:
        </p>
        <h5><span>Be Prepared</span></h5>
        <p>
          When you arrive for your NCLEX, you'll need your Accepted ID You will
          no longer need to bring a paper copy of the Authorization to Test
          (ATT) for admittance to the NCLEX. Important Exam Day Tips
        </p>
        <ol>
          <li>
            Make sure to bring your ID. Candidates without proper ID will not be
            allowed to take the NCLEX.
          </li>
          <li>
            Know the NCLEX Candidate Rules! These include policies specific to
            personal items, confidentiality, NCLEX administration and break
            procedures.
          </li>
        </ol>

        <h5><span>Dress comfortably.</span></h5>
        <p>
          You're required to leave hats, scarves, gloves and coats outside of
          the testing room. (Provisions are made for religious/cultural dress.)
        </p>

        <h5><span>Arrive Early.</span></h5>
        <p>
          Plan to arrive 30 minutes before your exam. If you are more than 30
          minutes late, you may have to forfeit, reregister and pay another exam
          fee.
        </p>
        <h5><span>Provide Your Biometrics.</span></h5>
        <p>
          In order to be tested, the following biometrics will be taken:
          signature, photograph and palm vein scan.
        </p>
        <h5><span>Receive Biometrics.</span></h5>
        <p>
          You may not bring in paper or writing instruments for this
          computer-administered exam. An on-screen calculator and erasable note
          board/marker for making notes will be provided.
        </p>
        <h5><span>Set Your Pace.</span></h5>
        <p>
          You'll have up to 6 hours to complete the RN exam, including the short
          tutorial and 2 optional breaks (the first 2 hours into testing, the
          second after 3.5 hours of testing). Take time to analyze each question
          carefully—once you submit an answer you can't return to that question.
          Learn more about computer adaptive testing (CAT).
        </p>
        <h5><span>Special Research Section.</span></h5>
        <p>
          Since July 2017, NCSBN started presenting a Special Research Section
          as part of the NCLEX-RN administration. By participating in the
          Special Research Section, candidates will be making valuable
          contributions to the future development of NCSBN examinations as well
          as to the enhancement of the nursing profession.
        </p>
        <p>
          The Special Research Section is given to select candidates taking the
          NCLEX-RN and takes approximately 30 minutes to complete. This section
          is administered following the regular exam and does not count as part
          of the NCLEX score.
        </p>
        <p>
          Once a candidate finishes their exam, an introductory screen indicates
          the beginning of the Special Research Section. This section will also
          continue to be numbered in accordance with the completed exam – for
          example, if a candidate's exam ended with question 153, the first
          question on the Special Research Section will be numbered 154. Despite
          the consecutive numbering, these new questions will have no impact on
          NCLEX scoring or results.
        </p>
        <p>
          Candidates may take the entire allotted six hours to complete the
          NCLEX. All questions on the NCLEX-RN and the Special Research Section
          are confidential.
        </p>
        <h5><span>Finishing up.</span></h5>
        <p>
          You'll end with a short computerized survey. Afterwards, raise your
          hand and wait for the TA to dismiss you.
        </p>
        <p>
          Keep in mind that although the exam is graded as you progress, scores
          go through a second verification process at Pearson VUE. Your score is
          not available and will not be released at the test center.
        </p>
